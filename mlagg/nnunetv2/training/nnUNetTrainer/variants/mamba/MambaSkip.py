class UnetResBlock(nn.Module):
    """
    A skip-connection based module that can be used for DynUNet, based on:
    `Automated Design of Deep Learning Methods for Biomedical Image Segmentation <https://arxiv.org/abs/1904.08128>`_.
    `nnU-Net: Self-adapting Framework for U-Net-Based Medical Image Segmentation <https://arxiv.org/abs/1809.10486>`_.

    Args:
        spatial_dims: number of spatial dimensions.
        in_channels: number of input channels.
        out_channels: number of output channels.
        kernel_size: convolution kernel size.
        stride: convolution stride.
        norm_name: feature normalization type and arguments.
        act_name: activation layer type and arguments.
        dropout: dropout probability.

    """

    def __init__(
        self,
        spatial_dims: int,
        in_channels: int,
        out_channels: int,
        kernel_size: Sequence[int] | int,
        stride: Sequence[int] | int,
        norm_name: tuple | str,
        act_name: nn.Module = nn.SiLU,
        dropout: tuple | str | float | None = None,
    ):
        super().__init__()
        self.conv1 = get_conv_layer(
            spatial_dims,
            in_channels,
            out_channels,
            kernel_size=kernel_size,
            stride=stride,
            dropout=dropout,
            act=None,
            norm=None,
            conv_only=False,
        )
        self.conv2 = get_conv_layer(
            spatial_dims,
            out_channels,
            out_channels,
            kernel_size=kernel_size,
            stride=1,
            dropout=dropout,
            act=None,
            norm=None,
            conv_only=False,
        )
        self.act = act_name()
        self.norm1 = get_norm_layer(name=norm_name, spatial_dims=spatial_dims, channels=out_channels)
        self.norm2 = get_norm_layer(name=norm_name, spatial_dims=spatial_dims, channels=out_channels)
        self.downsample = in_channels != out_channels
        stride_np = np.atleast_1d(stride)
        if not np.all(stride_np == 1):
            self.downsample = True
        if self.downsample:
            self.conv3 = get_conv_layer(
                spatial_dims,
                in_channels,
                out_channels,
                kernel_size=1,
                stride=stride,
                dropout=dropout,
                act=None,
                norm=None,
                conv_only=False,
            )
            self.norm3 = get_norm_layer(name=norm_name, spatial_dims=spatial_dims, channels=out_channels)

    def forward(self, inp):
        residual = inp
        out = self.conv1(inp)
        out = self.norm1(out)
        out = self.act(out)
        out = self.conv2(out)
        out = self.norm2(out)
        if hasattr(self, "conv3"):
            residual = self.conv3(residual)
        if hasattr(self, "norm3"):
            residual = self.norm3(residual)
        out += residual
        out = self.act(out)
        return out

class VSS_Conv_Block(nn.Module):
    def __init__(
            self,
            feature_dims,
            hidden_dim: int = 0,
            drop_path: float = 0,
            norm_layer: Callable[..., torch.nn.Module] = partial(nn.LayerNorm, eps=1e-6),
            attn_drop_rate: float = 0,
            d_state: int = 16,
            ssm_ratio: int = 2.,
            **kwargs,
    ):
        super().__init__()

        ### mamba branch
        self.feature_dims = feature_dims
        self.hidden_dim = hidden_dim
        self.ln_1 = norm_layer(hidden_dim)
        self.self_attention = SS2D_skip(stage_num=len(feature_dims), d_model=hidden_dim, d_state=d_state, expand=ssm_ratio, dropout=attn_drop_rate,   **kwargs)
        self.drop_path = DropPath(drop_path)
        mlp_hidden_dim = int(hidden_dim * 4)
        self.norm2 = norm_layer(hidden_dim)
        self.mlps = nn.ModuleList()
        for i in range(len(feature_dims)):
            self.mlps.append(ConvolutionalGLU(in_features=hidden_dim, hidden_features=mlp_hidden_dim, act_layer=nn.SiLU))

        ### conv branch
        # self.feature_dims = feature_dims
        self.conv_dims = [feature_dim - hidden_dim for feature_dim in feature_dims]
        self.conv_branches = nn.ModuleList()
        for i in range(len(self.conv_dims)):
            self.conv_branches.append(
                nn.Sequential(
                    # ============== Implementation 1 (mine)
                    nn.Conv2d(in_channels=self.conv_dims[i], out_channels=self.conv_dims[i], kernel_size=3, stride=1,
                              padding=1),
                    nn.InstanceNorm2d(self.conv_dims[i], affine=True),
                    nn.SiLU()
                    # ============== Implementation 2 (monai)
                    # UnetResBlock(
                    #     spatial_dims=2,
                    #     in_channels=self.conv_dims[i],
                    #     out_channels=self.conv_dims[i],
                    #     kernel_size=3,
                    #     stride=1,
                    #     norm_name='instance',
                    #     act_name=nn.SiLU
                    # )
                )
            )

    def forward(self, inputs):
        ### inputs is a list with feature shape (B, C, H, W)
        B = inputs[0].shape[0]
        H = []
        W = []
        L_split = []
        m_branch = []
        c_branch = []
        for i in range(len(inputs)):
            _, _, h, w = inputs[i].shape
            H.append(h)
            W.append(w)
            L_split.append(h * w)
            m_feature, c_feature = torch.split(inputs[i], [self.hidden_dim, self.conv_dims[i]], dim=1)
            m_branch.append(m_feature)
            c_branch.append(c_feature)

        m_branch = [torch.flatten(m, 2) for m in m_branch]
        m_branch = torch.cat(m_branch, dim=-1)
        m_branch = m_branch.permute(0, 2, 1).contiguous()

        m_branch = m_branch + self.drop_path(self.self_attention(self.ln_1(m_branch), B, H, W, L_split))
        m_branch = self.norm2(m_branch)
        m_branch = list(torch.split(m_branch, L_split, dim=1))
        for i in range(len(m_branch)):
            m_branch[i] = m_branch[i] + self.drop_path(self.mlps[i](m_branch[i], H[i], W[i]))           # B, L[i], C
            m_branch[i] = m_branch[i].transpose(1, 2).view(B, -1, H[i], W[i]).contiguous()

        for i in range(len(c_branch)):
            c_branch[i] = self.conv_branches[i](c_branch[i])
            ##### name m_branch as output
            m_branch[i] = torch.cat([m_branch[i], c_branch[i]], dim=1)

        return m_branch


class VSS_Conv_Layer(nn.Module):
    """ A basic Swin Transformer layer for one stage.
    Args:
        dim (int): Number of input channels.
        depth (int): Number of blocks.
        drop (float, optional): Dropout rate. Default: 0.0
        attn_drop (float, optional): Attention dropout rate. Default: 0.0
        drop_path (float | tuple[float], optional): Stochastic depth rate. Default: 0.0
        norm_layer (nn.Module, optional): Normalization layer. Default: nn.LayerNorm
        downsample (nn.Module | None, optional): Downsample layer at the end of the layer. Default: None
        use_checkpoint (bool): Whether to use checkpointing to save memory. Default: False.
    """

    def __init__(
            self,
            feature_dims,
            hidden_dim,
            depth=1,
            attn_drop=0.,
            drop_path=0.,
            norm_layer=nn.LayerNorm,
            use_checkpoint=False,
            d_state=16,
            ssm_ratio=2.,
            **kwargs,
    ):
        super().__init__()
        self.hidden_dim = hidden_dim
        self.use_checkpoint = use_checkpoint

        self.blocks = nn.ModuleList([
            VSS_Conv_Block(
                feature_dims=feature_dims,
                hidden_dim=hidden_dim,
                drop_path=drop_path[i] if isinstance(drop_path, list) else drop_path,
                norm_layer=norm_layer,
                attn_drop_rate=attn_drop,
                d_state=d_state,
                ssm_ratio=ssm_ratio,
            )
            for i in range(depth)])

    def forward(self, x):
        for blk in self.blocks:
            if self.use_checkpoint:
                x = checkpoint.checkpoint(blk, x)
            else:
                x = blk(x)
        return x


